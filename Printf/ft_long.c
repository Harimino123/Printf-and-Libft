#include "ft_printf.h"

int ft_long(int p)
{
	char	*o;
	int	i;
	int	j;
	
	j = 0;
	o = ft_itoa(p);
	while (o[j] != '\0')
	{
		write (1 ,&o[j],1);
		j++;
	}
	i = ft_strlen(o);
	free(o);
	return (i);
}
